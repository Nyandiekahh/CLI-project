# ANSI color codes.
class Colors:
    HEADER = '\033[95m'   # Purple
    OKBLUE = '\033[94m'   # Blue
    OKCYAN = '\033[96m'   # Cyan
    OKGREEN = '\033[92m'  # Green
    WARNING = '\033[93m'  # Yellow
    FAIL = '\033[91m'     # Red
    BOLD = '\033[1m'      # Bold
    UNDERLINE = '\033[4m' # Underline
    RESET = '\033[0m'     # Reset to default